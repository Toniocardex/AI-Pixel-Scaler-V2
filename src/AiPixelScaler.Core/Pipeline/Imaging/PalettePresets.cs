using SixLabors.ImageSharp.PixelFormats;

namespace AiPixelScaler.Core.Pipeline.Imaging;

/// <summary>
/// Palette retrò predefinite — usate da <see cref="PaletteMapper"/> per dare
/// un'estetica di console specifica agli sprite IA.
///
/// Tutti i colori sono opachi (A=255). Il chiamante può scegliere se applicare
/// dithering Floyd-Steinberg in OKLab via <see cref="PaletteMapper.DitherMode"/>.
/// </summary>
public static class PalettePresets
{
    public enum Preset
    {
        /// <summary>Quantizzazione automatica via K-Means in OKLab (palette adattiva).</summary>
        CleanAI,

        /// <summary>Game Boy DMG (1989) — 4 verdi.</summary>
        GameBoyDMG,

        /// <summary>PICO-8 fantasy console — 16 colori.</summary>
        Pico8,

        /// <summary>NES sprite subset — 16 colori rappresentativi.</summary>
        NES16,

        /// <summary>CGA modo 5 — 4 colori (PC anni '80).</summary>
        CGA4,

        /// <summary>Sweetie 16 — palette fan-favorite stile NES, 16 colori.</summary>
        Sweetie16,
    }

    public static IReadOnlyList<Rgba32> Get(Preset p) => p switch
    {
        Preset.GameBoyDMG => GameBoyDMG,
        Preset.Pico8      => Pico8,
        Preset.NES16      => NES16,
        Preset.CGA4       => CGA4,
        Preset.Sweetie16  => Sweetie16,
        _                 => Array.Empty<Rgba32>(),
    };

    public static readonly Rgba32[] GameBoyDMG =
    [
        new(0x0F, 0x38, 0x0F, 0xFF),  // darkest green
        new(0x30, 0x62, 0x30, 0xFF),
        new(0x8B, 0xAC, 0x0F, 0xFF),
        new(0x9B, 0xBC, 0x0F, 0xFF),  // lightest green
    ];

